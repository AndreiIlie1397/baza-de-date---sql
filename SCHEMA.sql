CREATE TABLE FMI 
(
  cod_facultate VARCHAR2(5) NOT NULL,
  adresa VARCHAR2(50) NOT NULL ,
  telefon VARCHAR2(15) NOT NULL,
  email varchar2(50),
  CONSTRAINT cod_facultate PRIMARY KEY (cod_facultate)
);

CREATE TABLE DOMENII
(
  cod_domeniu VARCHAR2(5) NOT NULL,
  denumire VARCHAR2(50) NOT NULL ,
  nr_studenti NUMBER(5) NOT NULL,
  nr_ani NUMBER(2) NOT NULL,
  licenta_master VARCHAR2(25) NOT NULL,
  CONSTRAINT cod_domeniu PRIMARY KEY (cod_domeniu)
);

CREATE TABLE SECRETARIATE
(
  cod_secretariat VARCHAR2(5) NOT NULL,
  program VARCHAR2(50) NOT NULL ,
  telefon VARCHAR2(15) NOT NULL,
  email varchar2(50),
  CONSTRAINT cod_secretariat PRIMARY KEY (cod_secretariat)
);

CREATE TABLE DEPARTAMENTE
(
  cod_dep VARCHAR2(5) NOT NULL,
  sala NUMBER(3) NOT NULL ,
  tip VARCHAR2(50) NOT NULL,
  CONSTRAINT cod_dep PRIMARY KEY (cod_dep)
);

CREATE TABLE PROFESORI
(
  cod_profesor VARCHAR2(5) NOT NULL,
  nume_p VARCHAR2(50) NOT NULL ,
  prenume_p VARCHAR2(50) NOT NULL,
  CONSTRAINT cod_profesor PRIMARY KEY (cod_profesor)
);

CREATE TABLE MATERII
(
  cod_materie VARCHAR2(5) NOT NULL,
  an NUMBER(2) NOT NULL ,
  denumire VARCHAR2(50) NOT NULL,
  nr_credite NUMBER(4,2) NOT NULL,
  optionalitate VARCHAR2(15) NOT NULL,
  CONSTRAINT cod_materie PRIMARY KEY (cod_materie)
);

CREATE TABLE EXAMENE
(
  cod_examen VARCHAR2(5) NOT NULL,
  data VARCHAR2(50) NOT NULL ,
  nota VARCHAR2(50) NOT NULL,
);

CREATE TABLE STUDENTI
(
  nr_matricol VARCHAR2(10) NOT NULL,
  CNP VARCHAR2(15) NOT NULL ,
  nume VARCHAR2(50) NOT NULL,
  prenume VARCHAR2(50) NOT NULL,
  adresa VARCHAR2(50) NOT NULL,
  telefon VARCHAR2(15) NOT NULL,
  medie_ant NUMBER(4,2) NOT NULL,
  email VARCHAR2(50),
  olimpic VARCHAR2(5),
  CONSTRAINT nr_matricol PRIMARY KEY (nr_matricol)
);

CREATE TABLE BURSE
(
  cod_bursa VARCHAR2(5) NOT NULL,
  tip_bursa VARCHAR2(50) NOT NULL ,
  medie_min NUMBER(4,2) NOT NULL,
  valoare NUMBER(6,2) NOT NULL,
  CONSTRAINT cod_bursa PRIMARY KEY (cod_bursa)
);





